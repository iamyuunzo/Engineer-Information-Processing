-- 아래는 <제품>(제품명, 단가, 제조사) 테이블을 대상으로 "A" 제조사에서 생산한 제품들의 '단가'보다 높은 '단가'를 가진 제품의 정보를 조회하는 SQL문이다.
-- 이때, 괄호 () 안에 알맞은 SQL문을 작성하시오.

SELECT 제품명, 단가, 제조사
FROM 제품
WHERE 단가 > () (SELECT AVG(단가) FROM 제품 WHERE 제조사 = 'A');

-- 정답: ALL
-- 해설: 서브쿼리에서 'A' 제조사에서 생산한 제품들의 평균 단가를 구한 후, 메인쿼리에서 그 단가보다 높은 단가를 가진 제품들을 조회하기 위해 ALL 연산자를 사용한다. 
-- ALL은 서브쿼리의 모든 결과와 비교하여 조건을 만족하는 행을 선택한다. (A보다 높은 단가를 가진 제품들을 찾기 위해 사용됨)