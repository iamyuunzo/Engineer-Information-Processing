#include<stdio.h>

int main() {
    printf("|%d|\n", 123);          // |123|
    printf("|%5d|\n", 123);         // |  123|
    printf("|%-5d|\n", 123);        // |123  |
    printf("|%05d|\n", 123);        // |00123|
    printf("|%6.1f|\n", 123.17);    // | 123.2|
    printf("|%07.2f|\n", 123.45);   // |0123.45|
    printf("|%3.1f|\n", 123.45);    // |123.5|
    return 0;
}


// Solving
// printf의 형식 지정자 기본 구조 : %[플래그][폭][.정밀도][길이]변환문자
// ex. %07.2f = %(서식 시작), 0(빈자리를 0으로 채움), 7(전체 폭), .2(소수점 이하 2자리), f(실수 출력)
// %5d => 5칸 중 오른쪽 정렬, %-5d => 5칸 중 왼쪽 정렬
// 폭보다 실제 숫자가 더 길면 다 표시됨. (폭은 최소 크기일 뿐, 작으면 무시됨.)